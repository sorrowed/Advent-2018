fn create_cloth() -> Vec<Vec<i32>> {
    // Get the coordinates and size of the cloth
    let left = INPUT.iter().map(|r| r.left()).min().unwrap();
    let right = INPUT.iter().map(|r| r.right()).max().unwrap();
    let top = INPUT.iter().map(|r| r.top()).min().unwrap();
    let bottom = INPUT.iter().map(|r| r.bottom()).max().unwrap();

    let width = right - left;
    let height = bottom - top;

    let mut cloth = vec![vec![0; height as usize]; width as usize];

    // Mark how many time each patch is used
    for r in INPUT.iter() {
        for x in r.left()..r.right() {
            for y in r.top()..r.bottom() {
                cloth[(x - left) as usize][(y - top) as usize] += 1;
            }
        }
    }
    cloth
}

pub fn first() -> i32 {
    let cloth = create_cloth();

    // Count all patches use more than once
    let mut area = 0;
    for r in cloth {
        for c in r {
            area = if c > 1 { area + 1 } else { area }
        }
    }
    area
}

/// Returns if all of r's patches has only a single claim
fn single_claim(r: &Rectangle, cloth: &Vec<Vec<i32>>) -> bool {
    let mut c = true;

    let mut x = r.left();

    while c && x < r.right() {
        let mut y = r.top();
        while c && y < r.bottom() {
            if cloth[x as usize][y as usize] > 1 {
                c = false;
            }
            y += 1;
        }
        x += 1;
    }
    c
}

pub fn second() -> i32 {
    let cloth = create_cloth();

    let mut id = -1;

    // Go through the claims and see if all patches have only one claim
    for r in INPUT.iter() {
        if single_claim(r, &cloth) {
            id = r.id;
        }
    }

    id
}
